#include "image.h"

const char *image_vs =
  "#version 330\n"
  "#define M_PI 3.1415926535897932384626433832795\n"
  "layout(location=0) in vec2 iPos;"
  "layout(location=1) in vec2 iTexCoord;"
  "out vec2 vTexCoord;"
  "uniform highp vec2 uResolution,uPosition;"
  "uniform highp float uRotation,uScale;"
  "uniform highp vec2 uPivot,uSize;"
  "vec2 v(vec2 u,float v)"
  "{"
    "float i=cos(v);"
    "v=sin(v);"
    "return vec2(u.x*i-u.y*v,u.y*i+u.x*v);"
  "}"
  "void main()"
  "{"
    "vTexCoord=iTexCoord;"
    "vec2 u=iPos;"
    "float i=uResolution.x/uResolution.y;"
    "vec2 n=vec2(uSize.x/uResolution.x*i,uSize.y/uResolution.y)*uScale;"
    "u=v((u-uPivot)*n,uRotation)+uPivot*n+vec2(uPosition.x/uResolution.x*i,uPosition.y/uResolution.y);"
    "u.y=-u.y;"
    "u=u*2-vec2(i,-1);"
    "u.x/=i;"
    "gl_Position=vec4(u,0,1);"
  "}";

